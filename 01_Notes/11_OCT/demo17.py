# methods for LIST

lst = [10, 17, 101, 12, 50 ]
print(lst)

lst.append(20) #append is use to add element in last of the list
print(lst)

lst.insert(4, 5) #insert any element to any index of the list
print(lst)

lst.pop()  #remove last element from list
print(lst)

lst.pop(0)  #remove 0index element from list
print(lst)

lst.remove(101) #remove the data 101
print(lst)

lst.clear() #clear all the elements from lst
print(lst)

lst.append(1) #
print(lst)

lst.insert(1, 2)
print(lst)

del lst
print(lst)

