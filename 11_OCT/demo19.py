#list method
lst = [4,22,3,14,88,5]
print("Original :", lst)

lst.sort() #arrange elements in ascending order
print("Sorted :", lst)

lst.sort(reverse=True) #arrange elements in descending order
print("Descending : ", lst)

lst = [4,22,3,14,88,5]
print("Original : ", lst)


lst.reverse() #to reverse the element of list 
print("Reversed :", lst)